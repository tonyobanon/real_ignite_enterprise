package com.re.paas.internal.core.fusion;

public class IntersectionNode {

	private String from;
	private String to;
	
	public String getFrom() {
		return from;
	}
	public IntersectionNode setFrom(String from) {
		this.from = from;
		return this;
	}
	public String getTo() {
		return to;
	}
	public IntersectionNode setTo(String to) {
		this.to = to;
		return this;
	}

}
