package com.re.paas.internal.clustering.classes;

public class NodeMetric {

	private double getSystemCpuLoad;
	private double getSystemFreeMemory;

	public double getGetSystemCpuLoad() {
		return getSystemCpuLoad;
	}

	public void setGetSystemCpuLoad(double getSystemCpuLoad) {
		this.getSystemCpuLoad = getSystemCpuLoad;
	}

	public double getGetSystemFreeMemory() {
		return getSystemFreeMemory;
	}

	public void setGetSystemFreeMemory(double getSystemFreeMemory) {
		this.getSystemFreeMemory = getSystemFreeMemory;
	}

}
